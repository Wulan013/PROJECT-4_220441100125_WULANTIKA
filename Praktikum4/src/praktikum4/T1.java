    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String matkul = tugas.getText();
        java.util.Date deadlineDate = kalender.getDate();
        String Detail = detail.getText();

        if (matkul.isEmpty() || deadlineDate == null || Detail.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Isi Tugas Terlebih Dahulu", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String deadline = dateFormat.format(deadlineDate);

            // Simpan data jika semua input telah diisi
            String namaDanTugas = nomor + ". " + matkul + " (dl: " + deadline + ")";
            listModel.addElement(namaDanTugas);
            detailModel.addElement(Detail);
            nomor++;
            JOptionPane.showMessageDialog(null, "Berhasil Disimpan", 
                    "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        

    }//GEN-LAST:event_simpanActionPerformed

