    private void listValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listValueChanged
        // TODO add your handling code here:
        if (!evt.getValueIsAdjusting()) {
            int selectedIndex = list.getSelectedIndex();
            if (selectedIndex != -1) {
                String DetailTugas = detailModel.getElementAt(selectedIndex);
                detailtugas.setText(DetailTugas);
            }
}
    }//GEN-LAST:event_listValueChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int[] selectedIndices = list.getSelectedIndices();
    
    if (selectedIndices.length > 0) {
        for (int i = selectedIndices.length - 1; i >= 0; i--) {
            listModel.removeElementAt(selectedIndices[i]);
            detailModel.removeElementAt(selectedIndices[i]);
            JOptionPane.showMessageDialog(null, "Berhasil Dibersihkan", 
                    "Info", JOptionPane.INFORMATION_MESSAGE);
        }

        for (int i = 0; i < listModel.getSize(); i++) {
            String existingItem = listModel.getElementAt(i);
            String[] parts = existingItem.split("\\. ");
            int existingNomor = i + 1;
            String newItem = existingNomor + ". " + parts[1];
            listModel.setElementAt(newItem, i);
        }
        detailtugas.setText("");
        nomor = 1;
    } else if (listModel.isEmpty() || detailModel == null || detailModel.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tugas tidak tersedia", "Peringatan", JOptionPane.WARNING_MESSAGE);
        
    } else {
        listModel.removeAllElements();
        detailModel.removeAllElements();
        detailtugas.setText(""); 
        nomor = 1;
       
        
    }
    
    

    }//GEN-LAST:event_jButton1ActionPerformed


